#include "libft.h"

char	*ft_strdup(const char *s)
{
	size_t	len;
	char	*dup;
	char	*start;

	len = 0;
	while (s[len])
		len++;
	dup = (char *)malloc(sizeof(char) * (len + 1));
	if (!dup)
		return (NULL);
	start = dup;
	while (*s)
		*dup++ = *s++;
	*dup = '\0';
	return (start);
}
